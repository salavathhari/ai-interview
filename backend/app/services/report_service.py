class ReportDocTemplate(BaseDocTemplate):
    """Custom doc template with page numbers, headers, and footers."""

    def __init__(self, filename, title="AI Career Report", **kwargs):
        self.report_title = title
        super().__init__(filename, **kwargs)
        frame = Frame(
            self.leftMargin, self.bottomMargin,
            self.width, self.height,
            id='normal'
        )
        template = PageTemplate(id='default', frames=frame, onPage=self._draw_header_footer)
        self.addPageTemplates([template])

    def _draw_header_footer(self, canvas, doc):
        canvas.saveState()
        page_width, page_height = letter

        # Header line
        canvas.setStrokeColor(colors.HexColor('#1e40af'))
        canvas.setLineWidth(1.5)
        canvas.line(self.leftMargin, page_height - 0.5 * inch,
                    page_width - self.rightMargin, page_height - 0.5 * inch)

        # Header text
        canvas.setFont('Helvetica', 8)
        canvas.setFillColor(colors.HexColor('#6b7280'))
        canvas.drawString(self.leftMargin, page_height - 0.45 * inch, self.report_title)
        canvas.drawRightString(page_width - self.rightMargin, page_height - 0.45 * inch,
                               "AI Career Preparation Platform")

        # Footer line
        canvas.setStrokeColor(colors.HexColor('#e5e7eb'))
        canvas.setLineWidth(0.5)
        canvas.line(self.leftMargin, 0.5 * inch,
                    page_width - self.rightMargin, 0.5 * inch)

        # Footer text
        canvas.setFont('Helvetica', 8)
        canvas.setFillColor(colors.HexColor('#9ca3af'))
        canvas.drawString(self.leftMargin, 0.35 * inch, "Generated by AI Career Preparation Platform")
        canvas.drawRightString(page_width - self.rightMargin, 0.35 * inch,
                               f"Page {doc.page}")

        canvas.restoreState()


