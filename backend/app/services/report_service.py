    @staticmethod
    def _capture_scores_snapshot(db: Session, user) -> dict:
        """Capture a snapshot of all current scores for the report."""
        from app.models.career import (
            ResumeAnalysis, SkillGapAnalysis, CareerReadiness
        )

        latest_analysis = db.query(ResumeAnalysis).filter(
            ResumeAnalysis.user_id == user.id
        ).order_by(ResumeAnalysis.created_at.desc()).first()
        latest_gap = db.query(SkillGapAnalysis).filter(
            SkillGapAnalysis.user_id == user.id
        ).order_by(SkillGapAnalysis.created_at.desc()).first()
        readiness = db.query(CareerReadiness).filter(
            CareerReadiness.user_id == user.id
        ).order_by(CareerReadiness.created_at.desc()).first()

        scores = {}
        if latest_analysis:
            if latest_analysis.resume_match_score is not None:
                scores["resume_match"] = latest_analysis.resume_match_score
            if latest_analysis.ats_score is not None:
                scores["ats_score"] = latest_analysis.ats_score
        if latest_gap and latest_gap.match_percentage is not None:
            scores["skill_gap_match"] = latest_gap.match_percentage
        if readiness:
            if readiness.overall_score is not None:
                scores["overall_readiness"] = readiness.overall_score
            if readiness.interview_score is not None:
                scores["interview_score"] = readiness.interview_score
            if readiness.coding_score is not None:
                scores["coding_score"] = readiness.coding_score
            if readiness.learning_score is not None:
                scores["learning_score"] = readiness.learning_score

        return scores

